    def build_level_json(lvl_id, layout):
        level_map = [[0 for _ in range(100)] for _ in range(100)]
        objects = []
        guards = []
        entrance = [64, 64]
        
        # АВТОЦЕНТРИРОВАНИЕ гигантских чертежей на карте 100x100
        map_h = len(layout)
        map_w = len(layout[0])
        offset_y = (100 - map_h) // 2
        offset_x = (100 - map_w) // 2

        obj_count = 0
        for r, row in enumerate(layout):
            for c, char in enumerate(row):
                world_x = (c + offset_x) * 64
                world_y = (r + offset_y) * 64
                
                if char == '#': level_map[r + offset_y][c + offset_x] = 2
                elif char in ['.', 'V', 'P', 'D', 'B', '1', '2', '3', '4', 'E']: level_map[r + offset_y][c + offset_x] = 1
                else: level_map[r + offset_y][c + offset_x] = 0
                
                if char == 'E': entrance = [world_x, world_y]
                elif char == 'V': objects.append({"name": f"obj_{obj_count}", "sprite_id": "spr_vase", "pos": [world_x, world_y]}); obj_count+=1
                elif char == 'P': objects.append({"name": f"obj_{obj_count}", "sprite_id": "spr_pic", "pos": [world_x, world_y]}); obj_count+=1
                elif char == 'D': objects.append({"name": f"obj_{obj_count}", "sprite_id": "spr_sofa", "pos": [world_x, world_y]}); obj_count+=1
                elif char == 'B': objects.append({"name": f"obj_{obj_count}", "sprite_id": "spr_bush", "pos": [world_x, world_y]}); obj_count+=1
                elif char == '1': guards.append({"type": "normal", "pos": [world_x, world_y]})
                elif char == '2': guards.append({"type": "fast", "pos": [world_x, world_y]})
                elif char == '3': guards.append({"type": "taser", "pos": [world_x, world_y]})
                elif char == '4': guards.append({"type": "random", "pos": [world_x, world_y]})

        guards_data = [{"type": g["type"], "x": g["pos"][0], "y": g["pos"][1]} for g in guards]
        
        data = {
            "entrance_pos": entrance,
            "tiles": tiles, "sprites": sprites, "map": level_map,
            "objects": objects, "guards_data": guards_data
        }

        with open(f"assets/levels/level_{lvl_id}.json", "w") as f:
            json.dump(data, f, indent=2)

